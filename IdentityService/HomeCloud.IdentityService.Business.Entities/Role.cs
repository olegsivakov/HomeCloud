namespace HomeCloud.IdentityService.Business.Entities
{
	/// <summary>
	/// Represents enumeration of user roles.
	/// </summary>
	public enum Role
	{
		/// <summary>
		/// The anonymous role.
		/// </summary>
		Anonymous = 0,

		/// <summary>
		/// The administrator role.
		/// </summary>
		Administrator = 1,

		/// <summary>
		/// The user role.
		/// </summary>
		User = 2
	}
}
